# Practice: Quick Brown Fox
# Use a for loop to take a list and print each element of the 
# list in its own line.

sentence = ["the", "quick", "brown", "fox", "jumped", "over", "the", "lazy", "dog"]

# Output:
# the
# quick
# brown
# fox
# jumped
# over
# the
# lazy
# dog

for word in range(len(sentence)):
    print(sentence[word])

